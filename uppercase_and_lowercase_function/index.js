//Simple Example

var a = "Pizza";
var b = "pizza";

if ( a == b){
    console.log("Match");
}
else{
    console.log("Not match.")
} 


//With toLowercase 

var a = "Pizza";
var b = "pizza";

if ( a.toLowerCase() == b.toLowerCase()){
    console.log("Match");
}
else{
    console.log("Not match.")
} 


//With touppercase

var a = "Pizza";
var b = "PIZZA";

if ( a.toUpperCase == b.toUpperCase){
    console.log("Match");
}
else{
    console.log("Not match.")
} 